.include "asm/include/battle_commands.inc"

.data

// Called by Soak.
_Start:
    Call BATTLE_SUBSCRIPT_ATTACK_MESSAGE_AND_ANIMATION
    GoToIfTerastallized BATTLER_CATEGORY_DEFENDER, _MoveFailed
    CompareMonDataToValue OPCODE_NEQ, BATTLER_CATEGORY_DEFENDER, BMON_DATA_TYPE_1, TYPE_WATER, _ChangeToWaterType
    CompareMonDataToValue OPCODE_NEQ, BATTLER_CATEGORY_DEFENDER, BMON_DATA_TYPE_2, TYPE_WATER, _ChangeToWaterType
    GoToIfThirdType BATTLER_CATEGORY_DEFENDER, TYPE_TYPELESS, _MoveFailed
    GoToIfThirdType BATTLER_CATEGORY_DEFENDER, TYPE_WATER, _MoveFailed

_ChangeToWaterType:
    UpdateMonData OPCODE_SET, BATTLER_CATEGORY_DEFENDER, BMON_DATA_TYPE_1, TYPE_WATER
    UpdateMonData OPCODE_SET, BATTLER_CATEGORY_DEFENDER, BMON_DATA_TYPE_2, TYPE_WATER
    AddThirdType TYPE_TYPELESS
    // {0} transformed into the Water type!
    PrintMessage 1330, TAG_NICKNAME_ABILITY, BATTLER_CATEGORY_MSG_TEMP, BATTLER_CATEGORY_MSG_BATTLER_TEMP
    GoTo _Cleanup

_MoveFailed:
    UpdateVar OPCODE_FLAG_ON, BSCRIPT_VAR_MOVE_STATUS_FLAGS, MOVE_STATUS_FAILED
    End 

_Cleanup:
    Wait 
    WaitButtonABTime 30
    End
